"""
E1 Experiment: Model Quality Evaluation (Offline, No Users)

This experiment evaluates the quality of state machines generated by MAS-LLM
by comparing them against reference models.

Metrics:
- F1_states: F1 score for state matching (threshold ≥ 0.80)
- F1_transitions: F1 score for transition matching (threshold ≥ 0.80)
- Proof_pass%: Percentage of successful verification proofs (threshold ≥ 90%)
- ED_graph: Normalized graph edit distance (threshold ≤ 10%)

Usage:
    python experiments/e1_model_quality.py [--model MODEL_NAME] [--verbose]
"""

