@dataclass
class E1ExperimentResult:
    """Result of a single E1 experiment run."""
    model_name: str
    metrics: Dict[str, Any]
    thresholds_met: Dict[str, bool]
    overall_success: bool
    generated_state_machine: Dict[str, Any]
    verification_report: Dict[str, Any]
    processing_time_ms: int
    timestamp: str
    error: Optional[str] = None


@dataclass
class E1ExperimentSummary:
    """Summary of all E1 experiment runs."""
    total_models: int
    successful_models: int
    failed_models: int
    average_f1_states: float
    average_f1_transitions: float
    average_proof_pass: float
    average_ed_graph: float
    results: List[E1ExperimentResult]
    timestamp: str


async def run_pipeline(requirements: str) -> Dict[str, Any]:
    """
    Run the full MAS-LLM pipeline on requirements.
    
    Returns dict with:
    - requirements: extracted requirements
    - logic_model: logic model
    - ontology: ontology model
    - state_machine: generated state machine
    - verification_report: verification results
    """
    result = {
        "requirements": {},
        "logic_model": {},
        "ontology": {},
        "state_machine": {},
        "verification_report": {},
    }
    
    # 1. LLM Agent - Extract requirements
    llm_agent = LLMAgent()
    llm_result = await llm_agent.execute({
        "task_type": "extract_requirements",
        "user_input": requirements
    })
    
    if llm_result["success"]:
        result["requirements"] = llm_result["result"]["requirements"]
    else:
        raise RuntimeError(f"LLM Agent failed: {llm_result.get('error_message')}")
    
    # 2. Logic Agent - Build logic model (needs requirements dict from LLM)
    logic_agent = LogicAgent()
    logic_result = await logic_agent.execute({
        "requirements": result["requirements"]
    })
    
    if logic_result["success"]:
        result["logic_model"] = logic_result["result"].get("logic_model", {})
    else:
        raise RuntimeError(f"Logic Agent failed: {logic_result.get('error_message')}")
    
    # 3. Class Agent - Generate ontology
    class_agent = ClassAgent()
    class_result = await class_agent.execute({
        "logic_model": result["logic_model"]
    })
    
    if class_result["success"]:
        result["ontology"] = class_result["result"].get("ontology", {})
    
    # 4. State Agent - Generate state machine (needs requirements for explicit transitions)
    state_agent = StateAgent()
    state_result = await state_agent.execute({
        "logic_model": result["logic_model"],
        "requirements": result["requirements"]
    })
    
    if state_result["success"]:
        result["state_machine"] = state_result["result"].get("state_machine", {})
    else:
        raise RuntimeError(f"State Agent failed: {state_result.get('error_message')}")
    
    # 5. Verifier Agent - Verify models
    verifier_agent = VerifierAgent()
    verifier_result = await verifier_agent.execute({
        "requirements": result["requirements"],
        "logic_model": result["logic_model"],
        "ontology": result["ontology"],
        "state_machine": result["state_machine"],
    })
    
    if verifier_result["success"]:
        result["verification_report"] = verifier_result["result"].get("verification_report", {})
    
    return result


async def run_single_experiment(
    reference_model: ReferenceModel,
    verbose: bool = False
) -> E1ExperimentResult:
    """Run E1 experiment on a single reference model."""
    
    start_time = datetime.now()
    
    if verbose:
        console.print(f"\n[bold cyan]Running experiment: {reference_model.name}[/bold cyan]")
    
    try:
        # Run the pipeline
        pipeline_result = await run_pipeline(reference_model.requirements)
        
        # Get generated state machine
        generated_sm = pipeline_result.get("state_machine", {})
        verification_report = pipeline_result.get("verification_report", {})
        
        # Get reference state machine
        reference_sm = reference_model.to_state_machine_dict()
        
        # Calculate metrics
        metrics = evaluate_model(
            generated=generated_sm,
            reference=reference_sm,
            verification_report=verification_report
        )
        
        processing_time = int((datetime.now() - start_time).total_seconds() * 1000)
        
        # Check thresholds
        thresholds = metrics.meets_e1_thresholds()
        
        result = E1ExperimentResult(
            model_name=reference_model.name,
            metrics=metrics.to_dict(),
            thresholds_met=thresholds,
            overall_success=thresholds["overall"],
            generated_state_machine=generated_sm,
            verification_report=verification_report,
            processing_time_ms=processing_time,
            timestamp=datetime.now().isoformat(),
        )
        
        if verbose:
            _display_single_result(result)
        
        return result
        
    except Exception as e:
        processing_time = int((datetime.now() - start_time).total_seconds() * 1000)
        
        return E1ExperimentResult(
            model_name=reference_model.name,
            metrics={},
            thresholds_met={},
            overall_success=False,
            generated_state_machine={},
            verification_report={},
            processing_time_ms=processing_time,
            timestamp=datetime.now().isoformat(),
            error=str(e),
        )


def _display_single_result(result: E1ExperimentResult) -> None:
    """Display result for a single experiment."""
    
    if result.error:
        console.print(f"[bold red]❌ Error: {result.error}[/bold red]")
        return
    
    metrics = result.metrics
    thresholds = result.thresholds_met
    
    table = Table(title=f"Results: {result.model_name}", box=box.ROUNDED)
    table.add_column("Metric", style="cyan")
    table.add_column("Value", style="white")
    table.add_column("Threshold", style="yellow")
    table.add_column("Status", style="white")
    
    table.add_row(
        "F1 States",
        f"{metrics.get('f1_states', 0):.3f}",
        "≥ 0.80",
        "✅" if thresholds.get("f1_states") else "❌"
    )
    table.add_row(
        "F1 Transitions",
        f"{metrics.get('f1_transitions', 0):.3f}",
        "≥ 0.80",
        "✅" if thresholds.get("f1_transitions") else "❌"
    )
    table.add_row(
        "Proof Pass %",
        f"{metrics.get('proof_pass_percentage', 0):.1%}",
        "≥ 90%",
        "✅" if thresholds.get("proof_pass") else "❌"
    )
    table.add_row(
        "ED Graph (norm)",
        f"{metrics.get('graph_edit_distance', 0):.3f}",
        "≤ 0.10",
        "✅" if thresholds.get("ed_graph") else "❌"
    )
    
    console.print(table)
    
    status = "[bold green]SUCCESS[/bold green]" if result.overall_success else "[bold red]FAILED[/bold red]"
    console.print(f"Overall: {status} | Time: {result.processing_time_ms}ms\n")


async def run_e1_experiment(
    model_names: Optional[List[str]] = None,
    verbose: bool = True,
    save_results: bool = True
) -> E1ExperimentSummary:
    """
    Run E1 experiment on all or selected reference models.
    
    Args:
        model_names: Optional list of model names to test. If None, tests all.
        verbose: If True, display detailed output.
        save_results: If True, save results to JSON file.
        
    Returns:
        E1ExperimentSummary with all results
    """
    
    console.print(Panel.fit(
        "[bold]E1 Experiment: Model Quality Evaluation[/bold]\n"
        "Comparing MAS-LLM generated state machines with reference models",
        title="🔬 Experiment E1",
        border_style="blue"
    ))
    
    # Load reference models
    reference_models = load_reference_models()
    
    # Filter by names if specified
    if model_names:
        reference_models = [m for m in reference_models if m.name in model_names]
    
    if not reference_models:
        console.print("[bold red]No reference models found![/bold red]")
        return None
    
    console.print(f"[dim]Testing {len(reference_models)} model(s)...[/dim]\n")
    
    # Run experiments
    results: List[E1ExperimentResult] = []
    for ref_model in reference_models:
        result = await run_single_experiment(ref_model, verbose=verbose)
        results.append(result)
    
    # Calculate summary
    successful = [r for r in results if r.overall_success]
    valid_results = [r for r in results if not r.error]
    
    if valid_results:
        avg_f1_states = sum(r.metrics.get("f1_states", 0) for r in valid_results) / len(valid_results)
        avg_f1_trans = sum(r.metrics.get("f1_transitions", 0) for r in valid_results) / len(valid_results)
        avg_proof = sum(r.metrics.get("proof_pass_percentage", 0) for r in valid_results) / len(valid_results)
        avg_ed = sum(r.metrics.get("graph_edit_distance", 0) for r in valid_results) / len(valid_results)
    else:
        avg_f1_states = avg_f1_trans = avg_proof = avg_ed = 0.0
    
    summary = E1ExperimentSummary(
        total_models=len(results),
        successful_models=len(successful),
        failed_models=len(results) - len(successful),
        average_f1_states=avg_f1_states,
        average_f1_transitions=avg_f1_trans,
        average_proof_pass=avg_proof,
        average_ed_graph=avg_ed,
        results=[asdict(r) for r in results],
        timestamp=datetime.now().isoformat(),
    )
    
    # Display summary
    _display_summary(summary)
    
    # Save results
    if save_results:
        output_path = Path(__file__).parent / "output" / "e1_results.json"
        output_path.parent.mkdir(parents=True, exist_ok=True)
        
        with open(output_path, "w", encoding="utf-8") as f:
            json.dump(asdict(summary), f, indent=2, ensure_ascii=False)
        
        console.print(f"\n[dim]Results saved to: {output_path}[/dim]")
    
    return summary


def _display_summary(summary: E1ExperimentSummary) -> None:
    """Display experiment summary."""
    
    console.print("\n")
    console.rule("[bold]E1 Experiment Summary[/bold]")
    
    # Summary table
    table = Table(box=box.ROUNDED)
    table.add_column("Metric", style="cyan")
    table.add_column("Average", style="white")
    table.add_column("Threshold", style="yellow")
    table.add_column("Status", style="white")
    
    table.add_row(
        "F1 States",
        f"{summary.average_f1_states:.3f}",
        "≥ 0.80",
        "✅" if summary.average_f1_states >= 0.80 else "❌"
    )
    table.add_row(
        "F1 Transitions",
        f"{summary.average_f1_transitions:.3f}",
        "≥ 0.80",
        "✅" if summary.average_f1_transitions >= 0.80 else "❌"
    )
    table.add_row(
        "Proof Pass %",
        f"{summary.average_proof_pass:.1%}",
        "≥ 90%",
        "✅" if summary.average_proof_pass >= 0.90 else "❌"
    )
    table.add_row(
        "ED Graph",
        f"{summary.average_ed_graph:.3f}",
        "≤ 0.10",
        "✅" if summary.average_ed_graph <= 0.10 else "❌"
    )
    
    console.print(table)
    
    # Overall stats
    success_rate = summary.successful_models / summary.total_models if summary.total_models > 0 else 0
    console.print(f"\n[bold]Models:[/bold] {summary.successful_models}/{summary.total_models} passed ({success_rate:.0%})")
    
    if summary.successful_models == summary.total_models:
        console.print("[bold green]🎉 All thresholds met! Experiment PASSED.[/bold green]")
    else:
        console.print(f"[bold yellow]⚠️ {summary.failed_models} model(s) failed to meet thresholds.[/bold yellow]")


